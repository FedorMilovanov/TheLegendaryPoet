import type { Essay, EssayBlock } from '../../types/essay';

function replaceParagraph(block: EssayBlock, needle: string, text: string): EssayBlock {
  if (block.type === 'paragraph' && block.text.includes(needle)) {
    return { ...block, text };
  }
  return block;
}

function polishYesenin(essay: Essay): Essay {
  const blocks = essay.blocks.map((initialBlock) => {
    let block = initialBlock;

    block = replaceParagraph(
      block,
      'Ответ нельзя свести ни к одному «сам захотел»',
      'Сначала скандал был инструментом. Имажинисты умели превращать афишу, выступление, переименованную улицу или роспись стены в новость; Есенин понимал цену дурной славы. Потом граница между ролью и жизнью стёрлась. Алкоголь, постоянное внимание публики и окружение сделали хулиганскую маску привычным способом существования. Поэт ещё мог описывать этот процесс со стороны, но всё хуже им управлял.',
    );

    block = replaceParagraph(
      block,
      'Историк должен отделять документы от итогового богословского вывода',
      'Документы не складываются в одну простую причину. В «кабацком» периоде сошлись сознательная литературная роль, алкогольная зависимость, общественный слом, личные кризисы и ухудшавшееся здоровье. Такая сложность не оправдывает пьянство и не делает финал случайностью. Она лишь не позволяет заменить биографию удобной формулой: Есенин долго участвовал в собственном разрушении, временами видел его с редкой ясностью и всё же не вышел из него.',
    );

    if (block.type === 'reflection') {
      block = {
        ...block,
        heading: 'Печаль, которая не стала покаянием',
        text: `Есенина жаль без всякой оговорки. Он обладал редким даром слышать человеческую и животную боль, любил мать, деревню, русскую речь. Эти качества были настоящими. Настоящими были и зависимость, скандалы, религиозный бунт и многолетнее саморазрушение. Сострадание не требует выбирать между двумя половинами биографии.

По сохранившимся текстам Есенин сознательно отверг путь Креста в «Инонии», позднее прямо назвал себя неверующим и не оставил ясного свидетельства возвращения ко Христу. Его смерть была самоубийством. Бог мог явить милость в последний миг, но исторических свидетельств такого обращения нет, и придумывать его ради более светлого финала нельзя. По известному исповеданию и плодам это был страшный, вероятнее всего погибельный конец неверующего человека.

Апостол Павел различает печаль, ведущую к покаянию, и печаль, производящую смерть (2 Кор. 7:10). Поздняя поэзия Есенина полна стыда, тоски и суда над собой; в ней не видно обращения этой печали ко Христу. Для живого читателя история остаётся предупреждением, а не приговором: дверь, которой поэт не вошёл, ещё открыта. «Придите ко Мне, все труждающиеся и обременённые, и Я успокою вас» (Мф. 11:28).`,
      };
    }

    return block;
  });

  return { ...essay, blocks };
}

function polishMayakovsky(essay: Essay): Essay {
  const blocks = essay.blocks.map((initialBlock) => {
    let block = initialBlock;

    block = replaceParagraph(
      block,
      'Историк не вправе выдавать богословскую оценку за медицинскую экспертизу',
      'Документы не дают одной причины самоубийства. Весной 1930 года сошлись жёсткая критика, творческий конфликт, личная драма, нервное истощение и давний страх одиночества. Богословская оценка не подменяет эту историю медицинским диагнозом. Но и множество причин не делает нравственный вывод невозможным: в опубликованных текстах остаются сознательное богохульство, вера в революционное переустройство и жизнь без видимых плодов покаяния.',
    );

    if (block.type === 'reflection') {
      block = {
        ...block,
        heading: 'Когда гром стих',
        text: `Маяковский хотел быть услышанным всей страной — и был услышан. Он изменил русскую поэтическую речь, научил строку звучать как площадь, реклама, спор и признание. Но в последнем кризисе ни известность, ни любовь, ни революционная роль не дали ему выхода.

Его смерть нельзя вывести из одного греха или одного разочарования. Всё же духовное направление жизни видно достаточно ясно. Маяковский публично хулил Бога, годами жил в прелюбодейной связи и связывал надежду с Революцией и огромным человеческим «я». Свидетельств покаяния и исповедания Христа перед смертью не сохранилось. Поэтому его конец следует считать не только человечески трагическим, но и духовно страшным — вероятным концом неверующего человека. Это говорится со скорбью, а не с чувством превосходства.

В предсмертном письме он написал: «у меня выходов нет». Евангелие обращается к тому, кто ещё может услышать: выход начинается не с усиления собственного голоса, а с примирения с Богом. Христос принимает и богохульника, и гордого, и отчаявшегося — не потому, что их бунт оказался красив, а потому, что Он прощает кающегося грешника.`,
      };
    }

    return block;
  });

  return { ...essay, blocks };
}

function polishBrik(essay: Essay): Essay {
  const blocks = essay.blocks.map((initialBlock) => {
    let block = initialBlock;

    block = replaceParagraph(
      block,
      'Христианская оценка здесь действительно ясна без сенсаций',
      'Нравственный смысл этой истории не зависит от самой громкой поздней сцены. Маяковский состоял в связи с женой другого человека; согласие Осипа меняло бытовую договорённость, но не превращало связь в брак. Письма показывают цену устроенной таким образом «свободы»: ревность, просьбы о любви, страх утраты и невозможность внутренне отделиться. Называть это прелюбодеянием точнее, чем прятать за выражением «сложные отношения», и честнее, чем выдумывать тайный заговор.',
    );

    if (block.type === 'reflection') {
      block = {
        ...block,
        heading: 'Свобода без верности',
        text: `Письма Маяковского к Лиле Брик сохраняют не схему «муж — жена — любовник», а повседневность многолетней зависимости: ожидание телеграммы, страх охлаждения, денежные поручения, подарки, просьбы подтвердить любовь. Творческая работа связывала этих людей всерьёз. Она же не делала устройство их союза добрым.

Связь с женой другого человека была прелюбодеянием. Осип мог согласиться с ней, участники могли называть её новой формой семьи, но новое название не отменяло отсутствия брачной верности. Обещанная свобода не избавила Маяковского от ревности и страха потери; она лишь оставила его без права ждать исключительной принадлежности от женщины, которую он считал своей семьёй.

Писание связывает любовь с истиной и верностью не ради холодного запрета, а ради защиты человека от такого рабства. Евангельский вывод здесь прост: грех не становится свободой после согласования, а прощение начинается не с удачной теории отношений, а с признания вины и примирения с Богом.`,
      };
    }

    return block;
  });

  return { ...essay, blocks };
}

export function applySourceVerificationRound7(essay: Essay): Essay {
  switch (essay.slug) {
    case 'yesenin-kutezhi':
      return polishYesenin(essay);
    case 'mayakovsky-gromovoy':
      return polishMayakovsky(essay);
    case 'brik-case':
      return polishBrik(essay);
    default:
      return essay;
  }
}
