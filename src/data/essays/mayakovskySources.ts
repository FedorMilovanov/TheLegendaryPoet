import type { EssaySource } from '../../types/essay';

const primary = (
  id: string,
  title: string,
  url: string,
  note?: string,
  year?: string | number,
): EssaySource => ({ id, title, url, note, year, kind: 'primary', institution: 'ФЭБ' });

const archive = (
  id: string,
  title: string,
  url: string,
  institution: string,
  note?: string,
  year?: string | number,
): EssaySource => ({ id, title, url, institution, note, year, kind: 'archive' });

const research = (
  id: string,
  title: string,
  url: string,
  institution: string,
  note?: string,
  year?: string | number,
): EssaySource => ({ id, title, url, institution, note, year, kind: 'research' });

const context = (
  id: string,
  title: string,
  url: string,
  institution: string,
  note?: string,
): EssaySource => ({ id, title, url, institution, note, kind: 'context' });

