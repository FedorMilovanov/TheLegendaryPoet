function patchMayakovsky(essay: Essay): Essay {
  const blocks = dedupeAdjacentSections(essay.blocks)
    .filter(
      (block) =>
        !(
          block.type === 'voice' &&
          /Государственный музей В\. В\. Маяковского/i.test(block.author)
        ),
    )
    .map((initialBlock) => {
      let block = initialBlock;

      block = replaceParagraph(
        block,
        'При этом религиозные образы у Маяковского',
        'Религиозные образы у Маяковского не исчезали: небо, звёзды, апостолы, воскресение и суд постоянно возвращались в его языке. Но сама напряжённость не является тайной верой. Чаще поэт присваивал библейский масштаб собственному «я», любви или революции, одновременно допуская прямое богохульство. По плодам текста перед нами не поиск послушания Христу, а человек, который знает религиозный язык и хочет занять в нём центральное место.',
      );

      block = replaceParagraph(
        block,
        'При этом историк не вправе объявлять одну духовную причину',
        'Историк не вправе выдавать богословскую оценку за медицинскую экспертизу: документы показывают соединение творческого, общественного, личного и эмоционального кризисов. Но из этого не следует, что духовный вывод запрещён. Христос велит судить по плодам. Программное богохульство, идолизация Революции, прелюбодейная связь, вера в титаническое «я» и отсутствие видимых плодов покаяния позволяют назвать явленное состояние прямо: огромный дар служил ложным богам и не дал человеку примирения с Творцом.',
      );

      if (block.type === 'reflection') {
        block = {
          ...block,
          heading: 'Идол, который потребовал голос',
          text: `Маяковский хотел говорить «во весь голос», построить новый мир и стать человеком огромного исторического масштаба. Его ложным спасителем была не одна женщина и не одна партия, а целая вера в Революцию и человеческое «я»: будто новый строй, сила воли, любовь и гениальность способны занять место Бога. Этот идол обещал царство справедливости, но потребовал от поэта становиться «на горло собственной песне» и в конце не смог ответить человеку, написавшему: «у меня выходов нет».

Нельзя свести самоубийство к одной духовной причине: в апреле 1930 года сошлись несколько тяжёлых кризисов. Но нельзя и спрятаться за сложностью причин, чтобы не вынести нравственного суждения. В опубликованных словах Маяковского есть сознательное богохульство; в жизни — длительная прелюбодейная связь и служение революционному идолу; в доступных свидетельствах нет ясного исповедания Христа и плодов покаяния. Бог мог спасти и в последний миг, но исторических оснований утверждать такое обращение нет. По явленным плодам его конец следует считать не только человечески трагическим, но и духовно страшным — вероятным концом неверующего человека.

Это говорится не с превосходством. Евангелие открыто и для богохульника: Савл гнал Христа и был помилован. Но милость пришла к нему не через оправдание собственного бунта, а через встречу с Господом, покаяние и новую жизнь. Маяковский оставил нам свидетельство огромной силы дара и столь же огромной беспомощности идолов. «Без Меня не можете делать ничего» (Ин. 15:5) — не литературный приём, а приговор человеческой самодостаточности.

Живому читателю выход есть. Когда любовь, дело жизни, политика или собственная сила становятся последней надеждой, они уже требуют поклонения и рано или поздно ломают поклонника. Христос зовёт не усилить своё «я», а примириться с Богом: «Придите ко Мне, все труждающиеся и обременённые, и Я успокою вас» (Мф. 11:28).`,
        };
      }

      return block;
    });

  return {
    ...essay,
    blocks,
    sources: museumLast(essay.sources),
  };
}

