export function applyEditorialReview(essay: Essay): Essay {
  switch (essay.slug) {
    case 'yesenin-kutezhi':
      return patchYesenin(essay);
    case 'mayakovsky-gromovoy':
      return patchMayakovsky(essay);
    case 'brik-case':
      return patchBrik(essay);
    default:
      return { ...essay, blocks: dedupeAdjacentSections(essay.blocks) };
  }
}
