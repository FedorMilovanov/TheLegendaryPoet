import type { Essay, EssayBlock, EssaySource } from '../../types/essay';

function replaceParagraph(block: EssayBlock, needle: string, text: string): EssayBlock {
  if (block.type === 'paragraph' && block.text.includes(needle)) {
    return { ...block, text };
  }
  return block;
}

function replacePoemNote(block: EssayBlock, title: string, note: string): EssayBlock {
  if (block.type === 'poem' && block.title === title) {
    return { ...block, note };
  }
  return block;
}

function dedupeAdjacentSections(blocks: EssayBlock[]): EssayBlock[] {
  return blocks.filter((block, index) => {
    if (block.type !== 'section') return true;
    const previous = blocks[index - 1];
    return !(
      previous?.type === 'section' &&
      previous.heading.trim().toLocaleLowerCase('ru-RU') ===
        block.heading.trim().toLocaleLowerCase('ru-RU')
    );
  });
}

function museumLast(sources?: EssaySource[]): EssaySource[] | undefined {
  if (!sources) return undefined;
  return [...sources].sort((a, b) => {
    const aMuseum = /музей|мемориаль/i.test(a.title) ? 1 : 0;
    const bMuseum = /музей|мемориаль/i.test(b.title) ? 1 : 0;
    return aMuseum - bMuseum;
  });
}

