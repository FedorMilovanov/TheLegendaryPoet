export function expandBrikCase(base: Essay): Essay {
  const insertionIndex = base.blocks.findIndex(
    (block) => block.type === 'section' && block.heading === 'Что можно сказать твёрдо',
  );

  if (insertionIndex < 0) {
    return {
      ...base,
      readTime: Math.max(base.readTime, 36),
      blocks: [...base.blocks, ...expansionBlocks],
    };
  }

  return {
    ...base,
    readTime: Math.max(base.readTime, 36),
    blocks: [
      ...base.blocks.slice(0, insertionIndex),
      ...expansionBlocks,
      ...base.blocks.slice(insertionIndex),
    ],
  };
}
