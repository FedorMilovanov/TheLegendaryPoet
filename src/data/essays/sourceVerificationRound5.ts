      'Маяковский зарабатывал выступлениями, журналистикой, рекламой и изданиями и регулярно участвовал в расходах общего дома. Переписка фиксирует денежные поручения, выплаты Осипу через Госиздат, крупные покупки и переговоры о парижском автомобиле в 1928 году. Поэтому материальная поддержка Бриков — установленная часть общего быта. Но документы о платежах ещё не раскрывают тайный мотив получателей: утверждение, будто поэта удерживали исключительно как источник денег, требует иных доказательств.',
