import fs from 'node:fs';

function replaceRequired(path: string, from: string, to: string): void {
  const current = fs.readFileSync(path, 'utf8');
  if (!current.includes(from)) {
    if (current.includes(to)) {
      console.log(`Already updated: ${path}`);
      return;
    }
    throw new Error(`Missing anchor in ${path}: ${from.slice(0, 160)}`);
  }
  fs.writeFileSync(path, current.replace(from, to), 'utf8');
  console.log(`Updated: ${path}`);
}

