import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();

function replaceRequired(relative: string, from: string, to: string): void {
  const file = path.join(root, relative);
  const current = fs.readFileSync(file, 'utf8');
  if (!current.includes(from)) {
    throw new Error(`Missing anchor in ${relative}: ${from.slice(0, 140)}`);
  }
  fs.writeFileSync(file, current.replace(from, to), 'utf8');
}

function replaceSection(relative: string, startHeading: string, endHeading: string, replacement: string): void {
  const file = path.join(root, relative);
  const current = fs.readFileSync(file, 'utf8');
  const start = current.indexOf(startHeading);
  const end = current.indexOf(endHeading, start + startHeading.length);
  if (start < 0 || end < 0) {
    throw new Error(`Missing section boundary in ${relative}: ${startHeading} -> ${endHeading}`);
  }
  fs.writeFileSync(file, current.slice(0, start) + replacement.trimEnd() + '\n\n' + current.slice(end), 'utf8');
}

